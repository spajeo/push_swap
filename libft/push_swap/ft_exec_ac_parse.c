#include "libft.h"

void			ft_exec_ac_parse(int *i, char **str, void (*f)(const char *))
{
	if (!i || !*i || !str || !*str || !**str || !f)
		return  ;
	while (*str)
	{
		f(*str);
		++(str);
		--*i;
	}
	return; 
}
