#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	char *p_s1;

	p_s1 = s1;
	while (*p_s1)
		p_s1++;
	while ((*p_s1++ = *s2++))
		;
	if (*p_s1)
		*p_s1 = 0;
	return (s1);
}
