#include "libft.h"

void	ft_putcharuni(wchar_t n)
{
	char buff[4];

	ft_bzero(buff, 4);
	if (0x7F < n && n <= 0x7FF)
	{
		buff[1] |= ((0b00111111 & n) | 0b10000000);
		buff[0] |= ((0b00001111 & (n >>= 6)) | 0b11000000);
	}
	else if (n <= 0xFFFF)
	{
		buff[2] |= ((0b00111111 & n) | 0b10000000);
		buff[1] |= ((0b00111111 & (n >>= 6)) | 0b10000000);
		buff[0] |= ((0b00001111 & (n >>= 6)) | 0b11100000);
	}
	else if (n <= 0x1FFFFF)
	{
		buff[3] |= ((0b00111111 & n) | 0b10000000);
		buff[2] |= ((0b00111111 & (n >>= 6)) | 0b10000000);
		buff[1] |= ((0b00111111 & (n >>= 6)) | 0b10000000);
		buff[0] |= ((0b00001111 & (n >>= 6)) | 0b11110000);
	}
	ft_putstr(buff);
}
