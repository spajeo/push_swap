#include "libft.h"

int		ft_buffwchar(char *buff, wchar_t n)
{
	int n_char;

	n_char = 1;
	if (n <= 0x7F && !(buff[1] = 0))
		buff[0] = n;
	else if (n <= 0x7FF && ft_bzero(buff, 3) && (n_char = 2))
	{
		buff[1] |= ((0b00111111 & n) | 0b10000000);
		buff[0] |= ((0b00011111 & (n >>= 6)) | 0b11000000);
	}
	else if (n <= 0xFFFF && ft_bzero(buff, 4) && (n_char = 3))
	{
		buff[2] |= ((0b00111111 & n) | 0b10000000);
		buff[1] |= ((0b00111111 & (n >>= 6)) | 0b10000000);
		buff[0] |= ((0b00001111 & (n >>= 6)) | 0b11100000);
	}
	else if (n <= 0x1FFFFF && ft_bzero(buff, 5) && (n_char = 4))
	{
		buff[3] |= ((0b00111111 & n) | 0b10000000);
		buff[2] |= ((0b00111111 & (n >>= 6)) | 0b10000000);
		buff[1] |= ((0b00111111 & (n >>= 6)) | 0b10000000);
		buff[0] |= ((0b00000111 & (n >>= 6)) | 0b11110000);
	}
	return (n_char);
}
